package pr2;

public class Book
{
    public String name;
    public String author;
    public int date;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
}
